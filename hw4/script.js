// 13 Currency exchange
{
    // Запитайте користувача про валюту та операцію (купівля чи продаж)
    let currency = prompt("Введіть назву валюти (наприклад, USD, EUR):").toUpperCase();
    let isBuying = confirm("Ви хочете купити цю валюту?");

    // Оголосіть змінну rate для збереження курсу конкретної валюти на купівлю чи продаж
    let rate;

    // Визначте курс для кожної валюти
    if (currency === "USD") {
        rate = isBuying ? 38.05 : 37.55;
    } else if (currency === "EUR") {
        rate = isBuying ? 42.15 : 41.45;
    } else if (currency === "GBP") {
        rate = isBuying ? 48.30 : 46.95;
    } else {
        // Якщо введена невідома валюта, виведіть повідомлення про помилку
        alert("Вибачте, але для введеної валюти курс не визначений.");
    }

    // Перевірте, чи користувач ввів коректну валюту
    if (rate !== undefined) {
        // Запитайте користувача про суму обміну
        let amount = parseFloat(prompt("Введіть суму для обміну:"));

        // Порахуйте та виведіть результат обміну
        if (!isNaN(amount)) {
            let result = isBuying ? amount / rate : amount * rate;
            alert("Результат обміну: " + result.toFixed(2) + " " + currency);
        } else {
            // Якщо користувач ввів некоректну суму, виведіть повідомлення про помилку
            alert("Введено некоректну суму.");
        }
    }
}


// 14 Scissors
{
    // Запитайте користувача про вибір
    let userChoice = prompt("Введіть свій вибір: камінь, ножиці або папір").toLowerCase();

    // Перевірте адекватність вводу
    if (userChoice !== "камінь" && userChoice !== "ножиці" && userChoice !== "папір") {
        alert("Введено некоректний вибір. Будь ласка, введіть камінь, ножиці або папір.");
    } else {
        // Генеруйте випадковий вибір комп'ютера
        let computerChoice = Math.random();
        if (computerChoice < 0.33) {
            computerChoice = "камінь";
        } else if (computerChoice < 0.67) {
            computerChoice = "ножиці";
        } else {
            computerChoice = "папір";
        }

        // Виведіть вибір комп'ютера
        alert("Комп'ютер обрав: " + computerChoice);

        // Визначте переможця або оголосіть нічию
        if (userChoice === computerChoice) {
            alert("Нічия!");
        } else if (
            (userChoice === "камінь" && computerChoice === "ножиці") ||
            (userChoice === "ножиці" && computerChoice === "папір") ||
            (userChoice === "папір" && computerChoice === "камінь")
        ) {
            alert("Ви переможець!");
        } else {
            alert("Комп'ютер переміг!");
        }
    }
}