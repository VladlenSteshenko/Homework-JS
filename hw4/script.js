// 2 String: lexics 
{
    // Запитайте користувача ввести текст
    let userText = prompt("Введіть текст:");

    // Масив некоректних слів, які ви шукаєте
    let forbiddenWords = ["bad", "inappropriate", "unacceptable"];

    // Змінна для визначення, чи знайдено некоректне слово
    let foundForbiddenWord = false;

    // Перевірте наявність некоректних слів у тексті
    for (let i = 0; i < forbiddenWords.length; i++) {
        if (userText.toLowerCase().indexOf(forbiddenWords[i]) !== -1) {
            foundForbiddenWord = true;
            break;
        }
    }

    // Виведіть відповідне повідомлення
    if (foundForbiddenWord) {
        console.log("У тексті знайдено некоректне слово.");
    } else {
    console.log("Текст є коректним.");
    }
}


